void	ft_bzero(void *s, int n)
{
	char	*t;
	int		i;

	if (n == 0)
	{
		return ;
	}
	t = (char *) s;
	i = 0;
	while (i < n)
	{
		t[i] = 0;
		i++;
	}
}
